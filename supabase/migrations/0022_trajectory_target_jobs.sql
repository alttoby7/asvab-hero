-- =============================================================
-- 0022_trajectory_target_jobs.sql
-- WS3 — Trajectory / commitment backend + MULTI-GOAL-JOB tracking.
--
-- Lets a user track UP TO 3 target jobs. For each, the home surface shows the
-- GAP between the user's CURRENT standing and the job's required scores AS A
-- BAND + confidence — NEVER an exact point delta. Reliability is low (~25
-- attempts in prod; a single diagnostic is ~±10 AFQT pts), so the contract
-- exposes only bands + status, never raw point gaps.
--
-- "Where they're testing at" is a DERIVED current score level, computed by
-- aggregating attempts.results_by_subtest (Beta posterior per subtest shrunk
-- toward 50), then running branch composites + PAY97 to emit low/point/high.
--
-- Tables: job_catalog, job_requirement_rules, user_target_jobs,
--         trajectory_score_snapshots (+ 3 profile columns).
-- RPCs:   rpc_refresh_trajectory_snapshot, rpc_get_home_trajectory,
--         rpc_add_target_job, rpc_remove_target_job, rpc_reorder_target_jobs,
--         rpc_search_job_catalog.
--
-- Catalog rows are seeded SEPARATELY from supabase/seed-jobs.sql (generated by
-- scripts/build-job-catalog.mjs). This migration only defines structure +
-- behavior; it does NOT insert catalog rows.
-- =============================================================

begin;

