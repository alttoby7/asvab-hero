-- =============================================================
-- rpc_refresh_trajectory_snapshot
-- Derives current_standing from AGGREGATED attempts.results_by_subtest.
-- Per AFQT subtest: Beta posterior (correct+a)/(seen+a+b) with a/b shrinking
-- the estimate toward 50 on the 20-62 standard scale; emit low/point/high
-- using the posterior +/- a Wilson-ish spread that narrows with more evidence.
-- Then PAY97 -> low/point/high AFQT. Confidence ramps with subtest coverage.
-- =============================================================
create or replace function public.rpc_refresh_trajectory_snapshot(
  p_snapshot_kind text default 'current_standing'
)
returns trajectory_score_snapshots
language plpgsql security definer set search_path = public
as $$
declare
  v_uid uuid := auth.uid();
  v_algo text := 'v1';

  -- aggregated subtest evidence: { ST: {seen, correct} }
  v_seen   jsonb := '{}'::jsonb;
  v_correct jsonb := '{}'::jsonb;

  v_attempt_count int := 0;
  v_latest timestamptz;

  -- 4 AFQT subtests on the EQUATED 20-62 scale (point/low/high)
  v_sub jsonb := '{}'::jsonb;        -- subtest_estimates output
  v_evidence jsonb := '{}'::jsonb;   -- per-subtest seen/correct for input_summary

  v_afqt_subs text[] := array['AR','MK','WK','PC'];
  v_st text;
  v_s int;
  v_c int;
  v_p numeric;        -- Beta posterior proportion
  v_spread numeric;   -- half-width on the proportion scale
  v_pt numeric; v_lo numeric; v_hi numeric;  -- equated 20-62 estimates

  -- VE = WK + PC ; raw = 2*VE + AR + MK -> separate low/point/high
  v_ve_pt numeric; v_ve_lo numeric; v_ve_hi numeric;
  v_raw_pt numeric; v_raw_lo numeric; v_raw_hi numeric;
  v_afqt_pt int; v_afqt_lo int; v_afqt_hi int;
  v_band record;

  -- branch composites (point estimate only; bands handle uncertainty)
  v_std jsonb := '{}'::jsonb;  -- all 9 subtests on equated scale, point est
  v_comp jsonb := '{}'::jsonb;

  v_afqt_covered int := 0;   -- how many AFQT subtests have evidence
  v_repeat_cov int := 0;     -- AFQT subtests with seen >= 8 (repeat coverage)
  v_conf text;

  v_result trajectory_score_snapshots;
begin
  if v_uid is null then
    raise exception 'not authenticated';
  end if;
  if p_snapshot_kind <> 'current_standing' then
    raise exception 'rpc_refresh_trajectory_snapshot only supports current_standing in v1';
  end if;

  -- Aggregate seen/correct per subtest across ALL of the user's attempts.
  select
    coalesce(jsonb_object_agg(k, seen_sum), '{}'::jsonb),
    coalesce(jsonb_object_agg(k, corr_sum), '{}'::jsonb)
  into v_seen, v_correct
  from (
    select rbs.key as k,
           sum((rbs.value->>'seen')::int)    as seen_sum,
           sum((rbs.value->>'correct')::int) as corr_sum
    from attempts a,
         jsonb_each(coalesce(a.results_by_subtest, '{}'::jsonb)) as rbs(key, value)
    where a.user_id = v_uid
    group by rbs.key
  ) agg;

  select count(*), max(completed_at)
    into v_attempt_count, v_latest
    from attempts where user_id = v_uid;

  -- Build per-subtest equated 20-62 estimates for all 9 subtests.
  foreach v_st in array array['GS','AR','WK','PC','MK','EI','AS','MC','AO'] loop
    v_s := coalesce((v_seen->>v_st)::int, 0);
    v_c := coalesce((v_correct->>v_st)::int, 0);

    -- Beta(2,2) prior centered at 0.5 -> shrinks sparse evidence toward 50.
    v_p := (v_c + 2.0) / (v_s + 4.0);
    -- spread (half-width) on proportion scale: ~0.25 with no data, narrows ~1/sqrt(n+4)
    v_spread := 0.5 / sqrt(v_s + 4.0);

    -- map proportion -> equated 20-62 standard score
    v_pt := 20 + v_p * 42;
    v_lo := 20 + greatest(0.0, v_p - v_spread) * 42;
    v_hi := 20 + least(1.0,  v_p + v_spread) * 42;

    v_std := v_std || jsonb_build_object(v_st, round(v_pt));

    if v_st = any(v_afqt_subs) then
      v_sub := v_sub || jsonb_build_object(v_st, jsonb_build_object(
        'low', round(v_lo), 'point', round(v_pt), 'high', round(v_hi),
        'seen', v_s, 'correct', v_c));
      v_evidence := v_evidence || jsonb_build_object(v_st,
        jsonb_build_object('seen', v_s, 'correct', v_c));
      if v_s > 0 then v_afqt_covered := v_afqt_covered + 1; end if;
      if v_s >= 8 then v_repeat_cov := v_repeat_cov + 1; end if;
    end if;
  end loop;

  -- AFQT low/point/high via PAY97 on the equated raw.
  v_ve_pt := (v_sub->'WK'->>'point')::numeric + (v_sub->'PC'->>'point')::numeric;
  v_ve_lo := (v_sub->'WK'->>'low')::numeric   + (v_sub->'PC'->>'low')::numeric;
  v_ve_hi := (v_sub->'WK'->>'high')::numeric  + (v_sub->'PC'->>'high')::numeric;

  v_raw_pt := 2*v_ve_pt + (v_sub->'AR'->>'point')::numeric + (v_sub->'MK'->>'point')::numeric;
  v_raw_lo := 2*v_ve_lo + (v_sub->'AR'->>'low')::numeric   + (v_sub->'MK'->>'low')::numeric;
  v_raw_hi := 2*v_ve_hi + (v_sub->'AR'->>'high')::numeric  + (v_sub->'MK'->>'high')::numeric;

  v_afqt_pt := traj_pay97_percentile(v_raw_pt);
  v_afqt_lo := traj_pay97_percentile(v_raw_lo);
  v_afqt_hi := traj_pay97_percentile(v_raw_hi);
  select * into v_band from traj_afqt_band(v_afqt_pt);

  -- Branch composites (point estimate; bands carry uncertainty downstream).
  v_comp := public.traj_branch_composites(v_std);

  -- Confidence: low ~ one diagnostic of AFQT evidence; medium once AFQT
  -- subtests have repeat coverage; high only after broad repeated coverage.
  if v_afqt_covered = 0 then
    v_conf := 'low';
  elsif v_repeat_cov >= 3 and v_afqt_covered = 4 then
    v_conf := 'high';
  elsif v_repeat_cov >= 1 then
    v_conf := 'medium';
  else
    v_conf := 'low';
  end if;

  insert into trajectory_score_snapshots as t (
    user_id, snapshot_kind, algorithm_version, generated_at, latest_attempt_at,
    attempt_count, afqt_point, afqt_low, afqt_high, afqt_band_key, afqt_band_label,
    overall_confidence, subtest_estimates, branch_composite_estimates, input_summary
  ) values (
    v_uid, p_snapshot_kind, v_algo, now(), v_latest,
    v_attempt_count, v_afqt_pt, v_afqt_lo, v_afqt_hi, v_band.band_key, v_band.band_label,
    v_conf, v_sub, v_comp,
    jsonb_build_object(
      'algorithm_version', v_algo,
      'attempt_count', v_attempt_count,
      'afqt_subtests_covered', v_afqt_covered,
      'afqt_subtests_repeat_covered', v_repeat_cov,
      'subtest_evidence', v_evidence,
      'equated_subtest_points', v_std
    )
  )
  on conflict (user_id, snapshot_kind) do update set
    algorithm_version          = excluded.algorithm_version,
    generated_at               = excluded.generated_at,
    latest_attempt_at          = excluded.latest_attempt_at,
    attempt_count              = excluded.attempt_count,
    afqt_point                 = excluded.afqt_point,
    afqt_low                   = excluded.afqt_low,
    afqt_high                  = excluded.afqt_high,
    afqt_band_key              = excluded.afqt_band_key,
    afqt_band_label            = excluded.afqt_band_label,
    overall_confidence         = excluded.overall_confidence,
    subtest_estimates          = excluded.subtest_estimates,
    branch_composite_estimates = excluded.branch_composite_estimates,
    input_summary              = excluded.input_summary
  returning * into v_result;

  return v_result;
end;
$$;

-- -------------------------------------------------------------
-- traj_branch_composites — compute composites for all 6 branches from a
-- jsonb of 9 equated subtest scores. Mirrors score-calculator.ts EXACTLY.
-- Returns { army:{GT:..}, navy:{...}, air_force:{...}, ... }.
-- AF/SF MAGE composites are raw sums (matches the lib); their job gaps are
-- marked beta at the catalog level because thresholds are percentiles.
-- -------------------------------------------------------------
create or replace function public.traj_branch_composites(p_std jsonb)
returns jsonb
language plpgsql immutable
as $$
declare
  gs numeric := coalesce((p_std->>'GS')::numeric, 0);
  ar numeric := coalesce((p_std->>'AR')::numeric, 0);
  wk numeric := coalesce((p_std->>'WK')::numeric, 0);
  pc numeric := coalesce((p_std->>'PC')::numeric, 0);
  mk numeric := coalesce((p_std->>'MK')::numeric, 0);
  ei numeric := coalesce((p_std->>'EI')::numeric, 0);
  aas numeric := coalesce((p_std->>'AS')::numeric, 0);
  mc numeric := coalesce((p_std->>'MC')::numeric, 0);
  ao numeric := coalesce((p_std->>'AO')::numeric, 0);
  ve numeric := coalesce((p_std->>'WK')::numeric, 0) + coalesce((p_std->>'PC')::numeric, 0);
  army jsonb; navy jsonb; af jsonb; mar jsonb;
begin
  army := jsonb_build_object(
    'GT', ar + ve, 'CL', ve + ar + mk, 'CO', ar + aas + mc,
    'EL', gs + ar + mk + ei, 'FA', ar + mk + mc, 'GM', gs + mc + aas,
    'MM', aas + mc + ei, 'OF', ve + ar + mc, 'SC', ve + ar + mk + ei,
    'ST', gs + ve + ar + mk);

  af := jsonb_build_object(
    'M', mc + 2*aas + gs, 'A', wk + pc + mk, 'G', ar + wk + pc,
    'E', gs + ar + mk + ei);

  mar := jsonb_build_object(
    'MM', ar + mc + aas + ei, 'GT', ar + wk + pc, 'EL', gs + ar + mk + ei);

  navy := jsonb_build_object(
    'VE', ve, 'AR', ar, 'MK', mk, 'GS', gs, 'EI', ei, 'AS', aas, 'MC', mc, 'AO', ao,
    'VE+AR', ve+ar, 'VE+MK', ve+mk, 'VE+GS', ve+gs, 'AR+MK', ar+mk,
    'VE+MK+GS', ve+mk+gs, 'AR+MC+AS', ar+mc+aas, 'GS+MC+EI', gs+mc+ei,
    'MK+EI+GS', mk+ei+gs, 'VE+MC+AS', ve+mc+aas, 'AR+MC+AS+EI', ar+mc+aas+ei,
    'AR+MK+EI+GS', ar+mk+ei+gs, 'AR+2MK+GS', ar+2*mk+gs,
    'VE+AR+MK+MC', ve+ar+mk+mc, 'VE+AR+MK+AS', ve+ar+mk+aas,
    'VE+AR+MK+GS', ve+ar+mk+gs, 'VE+AR+MK+AO', ve+ar+mk+ao,
    'VE+AR+MK+EI', ve+ar+mk+ei, 'AR+MK+MC+VE', ar+mk+mc+ve,
    'AR+GS+MC+EI', ar+gs+mc+ei);

  return jsonb_build_object(
    'army', army,
    'navy', navy,
    'coast_guard', navy,    -- coast guard uses navy scoring
    'air_force', af,
    'space_force', af,      -- space force uses MAGE
    'marines', mar
  );
end;
$$;

-- =============================================================
-- traj_eval_job_gap — band-only gap evaluation for ONE job.
-- Uses snapshot AFQT low/point/high + composite point estimates. For each
-- requirement: qualifies = low >= required; borderline = low<req<=high;
-- needs_work = high < required. Composites use a +/- one-band uncertainty
-- window derived from confidence (since composites only carry a point est).
-- NEVER returns raw point deltas.
-- =============================================================
create or replace function public.traj_eval_job_gap(
  p_job_id text,
  p_snapshot trajectory_score_snapshots
)
returns jsonb
language plpgsql stable
as $$
declare
  v_job job_catalog%rowtype;
  v_branch_comp jsonb;
  v_checks jsonb := '[]'::jsonb;
  v_rule record;
  v_actual numeric;
  v_req int;
  v_status text;
  v_gap_label text;
  v_band_actual int;
  v_band_req int;
  -- composite uncertainty window in band units, by confidence
  v_band_slack int;
  -- afqt
  v_afqt_status text := null;
  v_afqt_gap text := null;
  -- aggregation
  v_any_needs_work boolean := false;
  v_any_borderline boolean := false;
  -- OR-path tracking: group_id -> worst status within group
  v_groups jsonb := '{}'::jsonb;
  v_grp text;
  v_grp_state text;
  v_or_qualifies boolean := false;
  v_or_borderline boolean := false;
  v_has_or boolean := false;
  v_mandatory_qualifies boolean := true;
  v_overall text;
begin
  select * into v_job from job_catalog where id = p_job_id;
  if not found then
    return jsonb_build_object('error', 'job_not_found', 'job_id', p_job_id);
  end if;

  v_branch_comp := coalesce(p_snapshot.branch_composite_estimates -> v_job.branch, '{}'::jsonb);

  -- composite slack (in 10-pt bands): wider when confidence is low.
  v_band_slack := case p_snapshot.overall_confidence
    when 'high' then 0 when 'medium' then 1 else 2 end;

  -- AFQT requirement (banded).
  if v_job.min_afqt is not null then
    if p_snapshot.afqt_low >= v_job.min_afqt then
      v_afqt_status := 'qualifies';
    elsif p_snapshot.afqt_high >= v_job.min_afqt then
      v_afqt_status := 'borderline';
    else
      v_afqt_status := 'needs_work';
    end if;
    -- banded gap label (AFQT bands), by band-index distance.
    v_band_actual := traj_afqt_band_index(p_snapshot.afqt_point);
    v_band_req    := traj_afqt_band_index(v_job.min_afqt);
    if v_band_actual >= v_band_req then
      v_gap_label := 'at_or_above_band';
    elsif v_band_req - v_band_actual = 1 then
      v_gap_label := 'within_one_band';
    else
      v_gap_label := 'more_than_one_band_below';
    end if;
    v_afqt_gap := v_gap_label;
    if v_afqt_status = 'needs_work' then v_any_needs_work := true; v_mandatory_qualifies := false;
    elsif v_afqt_status = 'borderline' then v_any_borderline := true; v_mandatory_qualifies := false;
    end if;
  end if;

  -- Composite requirement rules.
  for v_rule in
    select requirement_group, composite_code, min_score
    from job_requirement_rules
    where job_id = p_job_id
    order by requirement_group, composite_code
  loop
    v_actual := coalesce((v_branch_comp->>v_rule.composite_code)::numeric, 0);
    v_req := v_rule.min_score;
    v_band_actual := traj_composite_band(round(v_actual)::int);
    v_band_req := traj_composite_band(v_req);

    -- status with confidence-driven slack on the point estimate.
    if v_band_actual - v_band_slack >= v_band_req and v_actual >= v_req then
      v_status := 'qualifies';
    elsif v_band_actual + v_band_slack >= v_band_req then
      v_status := 'borderline';
    else
      v_status := 'needs_work';
    end if;

    -- banded gap label (composite bands)
    if v_band_actual >= v_band_req then
      v_gap_label := 'at_or_above_band';
    elsif v_band_req - v_band_actual = 1 then
      v_gap_label := 'within_one_band';
    else
      v_gap_label := 'more_than_one_band_below';
    end if;

    v_checks := v_checks || jsonb_build_object(
      'composite_code', v_rule.composite_code,
      'requirement_group', v_rule.requirement_group,
      'status', v_status,
      'gap_band', v_gap_label,
      'actual_band', v_band_actual,
      'required_band', v_band_req
    );

    if v_rule.requirement_group = 0 then
      if v_status = 'needs_work' then v_any_needs_work := true; v_mandatory_qualifies := false;
      elsif v_status = 'borderline' then v_any_borderline := true; v_mandatory_qualifies := false;
      end if;
    else
      v_has_or := true;
      v_grp := v_rule.requirement_group::text;
      v_grp_state := coalesce(v_groups->>v_grp, 'qualifies');
      -- worst-of within an OR group (all rules in the group must pass)
      if v_status = 'needs_work' then v_grp_state := 'needs_work';
      elsif v_status = 'borderline' and v_grp_state <> 'needs_work' then v_grp_state := 'borderline';
      end if;
      v_groups := v_groups || jsonb_build_object(v_grp, v_grp_state);
    end if;
  end loop;

  -- Evaluate OR groups: best-of across groups.
  if v_has_or then
    for v_grp in select jsonb_object_keys(v_groups) loop
      if (v_groups->>v_grp) = 'qualifies' then v_or_qualifies := true; end if;
      if (v_groups->>v_grp) in ('qualifies','borderline') then v_or_borderline := true; end if;
    end loop;
  end if;

  -- Overall job status.
  --   qualifies  = every mandatory qualifies AND (no OR groups OR >=1 fully qualifies)
  --   borderline = no mandatory needs_work but >=1 borderline (or OR reaches borderline+)
  --   needs_work = any mandatory needs_work and no alternate path reaches borderline+
  if v_mandatory_qualifies and (not v_has_or or v_or_qualifies) then
    v_overall := 'qualifies';
  elsif v_any_needs_work and not (v_has_or and v_or_borderline) then
    v_overall := 'needs_work';
  else
    v_overall := 'borderline';
  end if;

  -- Beta/unsupported jobs: never project false confidence.
  if v_job.support_status <> 'supported' then
    v_overall := 'unsupported';
  end if;

  return jsonb_build_object(
    'job_id', v_job.id,
    'branch', v_job.branch,
    'code', v_job.code,
    'title', v_job.title,
    'category', v_job.category,
    'support_status', v_job.support_status,
    'unsupported_reason', v_job.unsupported_reason,
    'overall_status', v_overall,
    'afqt', case when v_job.min_afqt is null then null else
      jsonb_build_object('status', v_afqt_status, 'gap_band', v_afqt_gap) end,
    'checks', v_checks
  );
end;
$$;

-- =============================================================
-- rpc_get_home_trajectory — single payload for the home surface.
-- Refreshes the snapshot if latest attempt is newer than the stored snapshot.
-- =============================================================
create or replace function public.rpc_get_home_trajectory()
returns jsonb
language plpgsql security definer set search_path = public
as $$
declare
  v_uid uuid := auth.uid();
  v_cur trajectory_score_snapshots;
  v_proj trajectory_score_snapshots;
  v_latest timestamptz;
  v_targets jsonb := '[]'::jsonb;
  v_tj record;
  v_eval jsonb;
begin
  if v_uid is null then
    raise exception 'not authenticated';
  end if;

  select * into v_cur from trajectory_score_snapshots
   where user_id = v_uid and snapshot_kind = 'current_standing';

  select max(completed_at) into v_latest from attempts where user_id = v_uid;

  -- Refresh if missing or if a newer attempt exists than the snapshot reflects.
  if v_cur.user_id is null
     or v_cur.latest_attempt_at is distinct from v_latest then
    v_cur := public.rpc_refresh_trajectory_snapshot('current_standing');
  end if;

  select * into v_proj from trajectory_score_snapshots
   where user_id = v_uid and snapshot_kind = 'projected_test_day';

  -- Ordered target jobs with evaluated gaps.
  for v_tj in
    select t.id as target_job_id, t.job_id, t.display_order, t.is_primary
    from user_target_jobs t
    where t.user_id = v_uid
    order by t.is_primary desc, t.display_order asc
  loop
    v_eval := public.traj_eval_job_gap(v_tj.job_id, v_cur);
    v_targets := v_targets || (v_eval || jsonb_build_object(
      'target_job_id', v_tj.target_job_id,
      'display_order', v_tj.display_order,
      'is_primary', v_tj.is_primary
    ));
  end loop;

  return jsonb_build_object(
    'algorithm_version', v_cur.algorithm_version,
    'current_standing', jsonb_build_object(
      'generated_at', v_cur.generated_at,
      'attempt_count', v_cur.attempt_count,
      'overall_confidence', v_cur.overall_confidence,
      'afqt_band_key', v_cur.afqt_band_key,
      'afqt_band_label', v_cur.afqt_band_label,
      'subtest_estimates', v_cur.subtest_estimates
      -- NOTE: raw afqt point/low/high intentionally omitted from the contract;
      -- WS4 renders bands + confidence only. (Stored for debugging.)
    ),
    'projected_test_day', case when v_proj.user_id is null then null else
      jsonb_build_object(
        'generated_at', v_proj.generated_at,
        'overall_confidence', v_proj.overall_confidence,
        'afqt_band_key', v_proj.afqt_band_key,
        'afqt_band_label', v_proj.afqt_band_label
      ) end,
    'target_jobs', v_targets
  );
end;
$$;

-- =============================================================
-- rpc_add_target_job — append a target job (cap 3).
-- =============================================================
create or replace function public.rpc_add_target_job(
  p_job_id text,
  p_make_primary boolean default false
)
returns user_target_jobs
language plpgsql security definer set search_path = public
as $$
declare
  v_uid uuid := auth.uid();
  v_count int;
  v_next_order smallint;
  v_row user_target_jobs;
begin
  if v_uid is null then raise exception 'not authenticated'; end if;
  if not exists (select 1 from job_catalog where id = p_job_id) then
    raise exception 'unknown job_id: %', p_job_id;
  end if;

  select count(*) into v_count from user_target_jobs where user_id = v_uid;
  if v_count >= 3 and not exists (
       select 1 from user_target_jobs where user_id = v_uid and job_id = p_job_id) then
    raise exception 'target job limit reached (max 3)';
  end if;

  select coalesce(max(display_order), -1) + 1 into v_next_order
    from user_target_jobs where user_id = v_uid;

  if p_make_primary then
    update user_target_jobs set is_primary = false, updated_at = now()
      where user_id = v_uid;
  end if;

  insert into user_target_jobs (user_id, job_id, display_order, is_primary)
  values (v_uid, p_job_id, v_next_order, p_make_primary)
  on conflict (user_id, job_id) do update set
    is_primary = (user_target_jobs.is_primary or excluded.is_primary),
    updated_at = now()
  returning * into v_row;

  return v_row;
end;
$$;

-- =============================================================
-- rpc_remove_target_job
-- =============================================================
create or replace function public.rpc_remove_target_job(
  p_target_job_id uuid
)
returns void
language plpgsql security definer set search_path = public
as $$
declare
  v_uid uuid := auth.uid();
begin
  if v_uid is null then raise exception 'not authenticated'; end if;
  delete from user_target_jobs
    where id = p_target_job_id and user_id = v_uid;
end;
$$;

-- =============================================================
-- rpc_reorder_target_jobs — set display_order from array position;
-- first element becomes primary.
-- =============================================================
create or replace function public.rpc_reorder_target_jobs(
  p_target_job_ids uuid[]
)
returns void
language plpgsql security definer set search_path = public
as $$
declare
  v_uid uuid := auth.uid();
  v_id uuid;
  v_idx int := 0;
begin
  if v_uid is null then raise exception 'not authenticated'; end if;

  -- deferred unique(user_id, display_order) lets us reassign within one tx.
  set constraints all deferred;

  -- Park all rows out of the way first to avoid transient collisions.
  update user_target_jobs set display_order = display_order + 1000
    where user_id = v_uid;

  foreach v_id in array p_target_job_ids loop
    update user_target_jobs
      set display_order = v_idx,
          is_primary = (v_idx = 0),
          updated_at = now()
      where id = v_id and user_id = v_uid;
    v_idx := v_idx + 1;
  end loop;
end;
$$;

-- =============================================================
-- rpc_search_job_catalog — catalog search for the WS4 picker.
-- =============================================================
create or replace function public.rpc_search_job_catalog(
  p_branch text default null,
  p_query  text default null,
  p_limit  int  default 25
)
returns setof job_catalog
language sql stable security definer set search_path = public
as $$
  select *
  from job_catalog
  where (p_branch is null or branch = p_branch)
    and (
      p_query is null or p_query = ''
      or title ilike '%' || p_query || '%'
      or code  ilike '%' || p_query || '%'
      or category ilike '%' || p_query || '%'
    )
  order by
    case support_status when 'supported' then 0 when 'beta' then 1 else 2 end,
    branch, code
  limit greatest(1, least(coalesce(p_limit, 25), 100));
$$;

