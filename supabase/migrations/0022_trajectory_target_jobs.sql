-- -------------------------------------------------------------
-- traj_afqt_band — banded AFQT label (mirror gap-bands.ts AFQT_BANDS).
-- Bands: 0-9,10-20,21-30,31-49,50-64,65-92,93-99.
-- -------------------------------------------------------------
create or replace function public.traj_afqt_band(p_afqt int)
returns table (band_key text, band_label text)
language sql immutable
as $$
  select case
    when p_afqt <= 9  then 'afqt_0_9'
    when p_afqt <= 20 then 'afqt_10_20'
    when p_afqt <= 30 then 'afqt_21_30'
    when p_afqt <= 49 then 'afqt_31_49'
    when p_afqt <= 64 then 'afqt_50_64'
    when p_afqt <= 92 then 'afqt_65_92'
    else                   'afqt_93_99'
  end,
  case
    when p_afqt <= 9  then '0-9'
    when p_afqt <= 20 then '10-20'
    when p_afqt <= 30 then '21-30'
    when p_afqt <= 49 then '31-49'
    when p_afqt <= 64 then '50-64'
    when p_afqt <= 92 then '65-92'
    else                   '93-99'
  end;
$$;

