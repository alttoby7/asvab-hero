#!/usr/bin/env node
/**
 * WS3 — Job catalog seed generator.
 *
 * Reads src/data/{army,navy,marines,air-force,space-force,coast-guard}-jobs.json
 * and emits supabase/seed-jobs.sql: idempotent upserts into job_catalog +
 * job_requirement_rules.
 *
 * Mapping (mirrors src/lib/trajectory/catalog.ts toRequirementRules):
 *   - requirements[] -> requirement_group 0  (mandatory ALL-OF)
 *   - anyOf[]        -> requirement_group 1..n (each alternate one OR group)
 *
 * Correctness gate: Air Force + Space Force jobs are stamped
 * support_status='beta' because their MAGE composites are raw sums but job
 * thresholds are 1-99 percentiles (MAGE normalization not implemented) — see
 * docs/scoring-model.md. WS4 renders those as "not yet reliable".
 *
 * Generates SQL only. Do NOT apply to prod from here; the seed is applied
 * centrally after migration 0022.
 */

import fs from "node:fs";
import path from "node:path";

const ROOT = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const DATA_DIR = path.join(ROOT, "src/data");
const OUT = path.join(ROOT, "supabase/seed-jobs.sql");

const SOURCE_VERSION = new Date().toISOString().slice(0, 10); // YYYY-MM-DD

// branch slug -> DB branch code
const BRANCHES = [
  ["army", "army"],
  ["navy", "navy"],
  ["marines", "marines"],
  ["air-force", "air_force"],
  ["space-force", "space_force"],
  ["coast-guard", "coast_guard"],
];

const MAGE_BRANCHES = new Set(["air_force", "space_force"]);
const MAGE_BETA_REASON =
  "Air Force / Space Force MAGE composites are raw sums, but job thresholds " +
  "are 1-99 percentiles (MAGE normalization not yet implemented), so gap " +
  "estimates are not yet reliable. See docs/scoring-model.md.";

function sqlStr(v) {
  if (v === null || v === undefined) return "null";
  return "'" + String(v).replace(/'/g, "''") + "'";
}
function sqlNum(v) {
  if (v === null || v === undefined || Number.isNaN(Number(v))) return "null";
  return String(Math.round(Number(v)));
}

function toRequirementRules(job) {
  const rows = [];
  for (const r of job.requirements ?? []) {
    rows.push({ group: 0, composite: r.composite, min: r.minScore });
  }
  if (Array.isArray(job.anyOf) && job.anyOf.length > 0) {
    job.anyOf.forEach((r, i) => {
      rows.push({ group: i + 1, composite: r.composite, min: r.minScore });
    });
  }
  return rows;
}

const catalogRows = [];
const ruleRows = [];
const seenIds = new Set();

for (const [slug, branch] of BRANCHES) {
  const file = path.join(DATA_DIR, `${slug}-jobs.json`);
  const jobs = JSON.parse(fs.readFileSync(file, "utf8"));
  const beta = MAGE_BRANCHES.has(branch);

  for (const job of jobs) {
    if (!job.id || !job.code || !job.title) {
      throw new Error(`${slug}: job missing id/code/title: ${JSON.stringify(job)}`);
    }
    if (seenIds.has(job.id)) {
      throw new Error(`Duplicate job id across catalog: ${job.id}`);
    }
    seenIds.add(job.id);

    catalogRows.push({
      id: job.id,
      branch,
      code: job.code,
      title: job.title,
      category: job.category ?? null,
      description: job.description ?? null,
      min_afqt: job.minAFQT ?? null,
      support_status: beta ? "beta" : "supported",
      unsupported_reason: beta ? MAGE_BETA_REASON : null,
      source_slug: slug,
      source_version: SOURCE_VERSION,
    });

    for (const r of toRequirementRules(job)) {
      ruleRows.push({ job_id: job.id, ...r });
    }
  }
}

// ── Emit SQL ──────────────────────────────────────────────────────────
const lines = [];
lines.push("-- =============================================================");
lines.push("-- supabase/seed-jobs.sql — GENERATED by scripts/build-job-catalog.mjs");
lines.push(`-- source_version: ${SOURCE_VERSION}`);
lines.push(`-- catalog rows: ${catalogRows.length} | requirement rules: ${ruleRows.length}`);
lines.push("-- Apply CENTRALLY after migration 0022. Idempotent (upsert + replace rules).");
lines.push("-- AF/SF jobs are support_status='beta' pending MAGE normalization.");
lines.push("-- =============================================================");
lines.push("");
lines.push("begin;");
lines.push("");

// Catalog upserts.
lines.push("-- job_catalog upserts");
for (const c of catalogRows) {
  lines.push(
    "insert into job_catalog (id, branch, code, title, category, description, " +
      "min_afqt, support_status, unsupported_reason, source_slug, source_version) values (" +
      [
        sqlStr(c.id),
        sqlStr(c.branch),
        sqlStr(c.code),
        sqlStr(c.title),
        sqlStr(c.category),
        sqlStr(c.description),
        sqlNum(c.min_afqt),
        sqlStr(c.support_status),
        sqlStr(c.unsupported_reason),
        sqlStr(c.source_slug),
        sqlStr(c.source_version),
      ].join(", ") +
      ")\non conflict (id) do update set " +
      [
        "branch = excluded.branch",
        "code = excluded.code",
        "title = excluded.title",
        "category = excluded.category",
        "description = excluded.description",
        "min_afqt = excluded.min_afqt",
        "support_status = excluded.support_status",
        "unsupported_reason = excluded.unsupported_reason",
        "source_slug = excluded.source_slug",
        "source_version = excluded.source_version",
        "updated_at = now()",
      ].join(", ") +
      ";"
  );
}

lines.push("");
lines.push("-- Replace requirement rules for the seeded jobs (idempotent re-seed).");
const idList = catalogRows.map((c) => sqlStr(c.id)).join(", ");
lines.push(`delete from job_requirement_rules where job_id in (${idList});`);
lines.push("");
lines.push("-- job_requirement_rules inserts");
for (const r of ruleRows) {
  lines.push(
    "insert into job_requirement_rules (job_id, requirement_group, composite_code, min_score) values (" +
      [sqlStr(r.job_id), sqlNum(r.group), sqlStr(r.composite), sqlNum(r.min)].join(", ") +
      ");"
  );
}

lines.push("");
lines.push("commit;");
lines.push("");

fs.writeFileSync(OUT, lines.join("\n"));
console.log(
  `Wrote ${OUT}\n  catalog rows: ${catalogRows.length}\n  requirement rules: ${ruleRows.length}\n  source_version: ${SOURCE_VERSION}`
);
